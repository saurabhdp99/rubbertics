function FormField({
  field,
  control,
  disabled,
  error,
  options,
  onAddOption,
  onRenameOption,
  onDeleteOption,
  isLoadingPincode,
}) {
  const baseInputClass = `w-full text-[13px] font-medium rounded-xl text-slate-800 border bg-white transition-all outline-none ${error ? 'border-red-300 focus:border-red-400' : 'border-slate-200 focus:border-emerald-500/50'
    } input-glow disabled:bg-slate-50 disabled:text-slate-500 disabled:cursor-not-allowed`;
  const inputClass = `${baseInputClass} px-4 py-3`;
  const isLocked = disabled || field.autoGenerated;
  const selectOptions = options || field.options || [];

  return (
    <label className={`flex flex-col gap-2 ${field.wide ? 'md:col-span-2 xl:col-span-3' : ''}`}>
      <span className="text-[11px] font-bold text-slate-400 uppercase tracking-widest">
        {field.label}
        {field.required && <span className="text-red-500 ml-1">*</span>}
      </span>
      <Controller
        control={control}
        name={field.key}
        render={({ field: { onChange, value, onBlur, ref } }) => {
          if (field.type === 'creatable-select' || field.type === 'select') {
            return (
              <EditableCreatableSelect
                value={value || selectOptions[0] || ''}
                options={selectOptions}
                disabled={disabled}
                placeholder={field.label}
                onChange={onChange}
                onAdd={onAddOption}
                onRename={onRenameOption}
                onDelete={onDeleteOption}
              />
            );
          }
          if (field.type === 'textarea') {
            return (
              <textarea
                value={value ?? ''}
                disabled={disabled}
                onChange={onChange}
                onBlur={onBlur}
                className={`${inputClass} min-h-28 resize-y`}
                placeholder={field.label}
                ref={ref}
              />
            );
          }
          if (field.type === 'date') {
            return (
              <DatePicker
                value={value ? parseDate(value) : null}
                isDisabled={isLocked}
                onChange={(dateVal) => onChange(dateVal ? dateVal.toString() : '')}
                className="w-full"
                aria-label={field.label}
              >
                <DateField.Group className={`${baseInputClass} flex items-center overflow-hidden h-[46px]`} fullWidth>
                  <DateField.Input className="flex-1 py-3 px-4 outline-none bg-transparent">
                    {(segment) => <DateField.Segment segment={segment} />}
                  </DateField.Input>
                  <DateField.Suffix className="pr-4">
                    <DatePicker.Trigger className="text-slate-500 hover:text-emerald-600 transition-colors">
                      <DatePicker.TriggerIndicator />
                    </DatePicker.Trigger>
                  </DateField.Suffix>
                </DateField.Group>
                <DatePicker.Popover>
                  <Calendar aria-label={field.label}>
                    <Calendar.Header>
                      <Calendar.YearPickerTrigger>
                        <Calendar.YearPickerTriggerHeading />
                        <Calendar.YearPickerTriggerIndicator />
                      </Calendar.YearPickerTrigger>
                      <Calendar.NavButton slot="previous" />
                      <Calendar.NavButton slot="next" />
                    </Calendar.Header>
                    <Calendar.Grid>
                      <Calendar.GridHeader>
                        {(day) => <Calendar.HeaderCell>{day}</Calendar.HeaderCell>}
                      </Calendar.GridHeader>
                      <Calendar.GridBody>{(date) => <Calendar.Cell date={date} />}</Calendar.GridBody>
                    </Calendar.Grid>
                    <Calendar.YearPickerGrid>
                      <Calendar.YearPickerGridBody>
                        {({ year }) => <Calendar.YearPickerCell year={year} />}
                      </Calendar.YearPickerGridBody>
                    </Calendar.YearPickerGrid>
                  </Calendar>
                </DatePicker.Popover>
              </DatePicker>
            );
          }
          return (
            <Input
              type={field.type || 'text'}
              step={field.type === 'number' ? '0.01' : undefined}
              value={value ?? ''}
              disabled={isLocked}
              onChange={onChange}
              onBlur={onBlur}
              className={inputClass}
              placeholder={field.autoGenerated ? 'Auto generated on create' : field.placeholder || field.label}
              aria-label={field.label}
              ref={ref}
            />
          );
        }}
      />
      {field.autoGenerated && !disabled && (
        <span className="text-[11px] font-semibold text-emerald-600">Auto generated and locked</span>
      )}
      {field.key === 'pinCode' && isLoadingPincode && (
        <span className="text-[11px] font-semibold text-emerald-600 animate-pulse">Fetching details...</span>
      )}
      {error && <span className="text-xs font-medium text-red-500">{error}</span>}
    </label>
  );
}

function PartyMasterForm({ mode, party, onBack }) {
  const {
    addParty: addPartyMaster,
    updateParty: updatePartyMaster,
    deleteParty: deletePartyMaster,
    getNextPartyCode,
    lookups: partyMasterLookups,
    addLookupOption: addPartyMasterLookupOption,
    renameLookupOption: renamePartyMasterLookupOption,
    deleteLookupOption: deletePartyMasterLookupOption,
  } = usePartyMasterStore();
  const { currentOrg, currentUser } = useAuthStore();

  const isView = mode === 'view';
  const isAdd = mode === 'add';

  const { control, handleSubmit: hookFormSubmit, reset, setValue, watch, getValues, formState: { errors, isSubmitting } } = useForm({
    resolver: zodResolver(partyMasterSchema),
    defaultValues: createInitialPartyForm(
      party,
      partyMasterLookups.partyCategory[0],
      getNextPartyCode
    )
  });

  useEffect(() => {
    reset(createInitialPartyForm(
      party,
      partyMasterLookups.partyCategory[0],
      getNextPartyCode
    ));
  }, [party, mode, reset, partyMasterLookups.partyCategory, getNextPartyCode]);

  const [cityOptions, setCityOptions] = useState([]);
  const [isLoadingPincode, setIsLoadingPincode] = useState(false);

  const pinCode = watch('pinCode');
  const partyCategory = watch('partyCategory');

  useEffect(() => {
    if (isAdd && partyCategory) {
      setValue('partyCode', getNextPartyCode(partyCategory));
    }
  }, [partyCategory, isAdd, getNextPartyCode, setValue]);

  useEffect(() => {
    const fetchPincodeDetails = async () => {
      if (mode === 'view') return;

      if (pinCode?.length === 6) {
        setIsLoadingPincode(true);
        try {
          const res = await fetch(`https://api.postalpincode.in/pincode/${pinCode}`);
          const data = await res.json();
          if (data && data[0]?.Status === 'Success') {
            const postOffices = data[0].PostOffice;
            const state = postOffices[0].State;
            const district = postOffices[0].District;
            const country = postOffices[0].Country || 'India';

            // Extract unique city/area names
            const areas = [...new Set(postOffices.map(po => po.Name))];

            setCityOptions(areas);

            let distanceVal = '';
            try {
              // Fetch coordinates from OpenStreetMap for Company PIN (401208) and Target PIN
              const [resCompany, resTarget] = await Promise.all([
                fetch(`https://nominatim.openstreetmap.org/search?postalcode=401208&country=india&format=json`),
                fetch(`https://nominatim.openstreetmap.org/search?postalcode=${pinCode}&country=india&format=json`)
              ]);
              const dataCompany = await resCompany.json();
              const dataTarget = await resTarget.json();

              if (dataCompany.length > 0 && dataTarget.length > 0) {
                const lat1 = parseFloat(dataCompany[0].lat);
                const lon1 = parseFloat(dataCompany[0].lon);
                const lat2 = parseFloat(dataTarget[0].lat);
                const lon2 = parseFloat(dataTarget[0].lon);

                // Use free OSRM (Open Source Routing Machine) API for actual driving road distance
                const osrmRes = await fetch(`https://router.project-osrm.org/route/v1/driving/${lon1},${lat1};${lon2},${lat2}?overview=false`);
                const osrmData = await osrmRes.json();

                if (osrmData.code === 'Ok' && osrmData.routes.length > 0) {
                  const meters = osrmData.routes[0].distance;
                  distanceVal = Math.round(meters / 1000);
                } else {
                  // Fallback to Haversine if routing fails
                  const R = 6371;
                  const dLat = (lat2 - lat1) * Math.PI / 180;
                  const dLon = (lon2 - lon1) * Math.PI / 180;
                  const a = Math.sin(dLat / 2) * Math.sin(dLat / 2) + Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) * Math.sin(dLon / 2) * Math.sin(dLon / 2);
                  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
                  distanceVal = Math.round((R * c) * 1.3);
                }
              }
            } catch (e) {
              console.error("Distance calculation failed", e);
            }

            setValue('state', state);
            setValue('district', district);
            setValue('country', country);

            const currentCity = getValues('city');
            setValue('city', areas.includes(currentCity) ? currentCity : areas[0]);

            if (distanceVal !== '') {
              setValue('transportDistance', distanceVal);
            }
          } else {
            setCityOptions([]);
            setValue('state', '');
            setValue('district', '');
            setValue('city', '');
            setValue('transportDistance', '');
          }
        } catch (error) {
          console.error("Failed to fetch pincode details", error);
        } finally {
          setIsLoadingPincode(false);
        }
      } else {
        // If pincode is removed or not 6 digits, clear the fields
        setCityOptions([]);
        const currentVals = getValues(['state', 'district', 'city', 'transportDistance']);
        if (currentVals.some(v => v)) {
          setValue('state', '');
          setValue('district', '');
          setValue('city', '');
          setValue('transportDistance', '');
        }
      }
    };

    const timeoutId = setTimeout(fetchPincodeDetails, 600);
    return () => clearTimeout(timeoutId);
  }, [pinCode, mode, setValue, getValues]);


  const groupedFields = PARTY_MASTER_SECTIONS.map(section => ({
    section,
    fields: PARTY_MASTER_FIELDS.filter(field => field.section === section),
  }));

  const onSubmit = async (data) => {
    if (isAdd) await addPartyMaster(data, currentOrg?.id, currentUser?.id);
    else await updatePartyMaster(party.id, data, currentUser?.id);
    onBack();
  };


  return (
    <div className="animate-slide-up">
      <div className="glass-card rounded-2xl shadow-xl overflow-hidden">
        <div className="flex flex-col lg:flex-row lg:items-center justify-between gap-4 px-6 py-5 border-b border-slate-100 bg-slate-50/80">
          <div className="flex items-center gap-4">
            <button
              onClick={onBack}
              className="w-11 h-11 rounded-xl bg-white border border-slate-200 text-slate-600 hover:text-emerald-600 hover:bg-emerald-50 hover:border-emerald-100 flex items-center justify-center transition-all"
              title="Back to table"
            >
              <ArrowLeft size={20} />
            </button>
            <div className="w-12 h-12 rounded-2xl flex items-center justify-center bg-emerald-50 border border-emerald-200 shadow-lg shadow-emerald-500/10">
              {isView ? <Eye size={24} className="text-emerald-600" /> : <ContactRound size={24} className="text-emerald-600" />}
            </div>
            <div>
              <h2 className="text-xl font-black text-slate-800 tracking-tight">
                {isView ? 'View Party Master' : isAdd ? 'Add Party Master' : 'Edit Party Master'}
              </h2>
              <p className="text-sm font-medium text-slate-500 mt-0.5">
                {watch('partyCode') || 'Party code will be generated automatically'}
              </p>
            </div>
          </div>

          <div className="flex gap-3">
            <button
              type="button"
              onClick={onBack}
              className="flex items-center justify-center gap-2 px-5 py-3 rounded-xl text-sm font-bold text-slate-600 border border-slate-200 bg-white hover:bg-slate-50 transition-all"
            >
              <X size={16} />
              Back
            </button>
            {!isView && (
              <button
                type="submit"
                form="party-master-page-form"
                className="btn-primary flex items-center justify-center gap-2 px-6 py-3 rounded-xl text-sm font-bold text-white shadow-lg shadow-emerald-500/30"
              >
                <Save size={16} />
                {isAdd ? 'Create Party' : 'Save Changes'}
              </button>
            )}
          </div>
        </div>

        <form id="party-master-page-form" onSubmit={hookFormSubmit(onSubmit)} className="p-6">
          <div className="flex flex-col gap-7">
            {groupedFields.map(group => (
              <section key={group.section} className="border-b border-slate-100 last:border-b-0 pb-7 last:pb-0">
                <div className="flex items-center gap-3 mb-4">
                  <div className="h-8 w-8 rounded-xl bg-emerald-50 border border-emerald-100 flex items-center justify-center">
                    <Tag size={15} className="text-emerald-600" />
                  </div>
                  <h3 className="text-sm font-black text-slate-700 uppercase tracking-widest">{group.section}</h3>
                </div>
                <div className="grid grid-cols-1 md:grid-cols-2 xl:grid-cols-3 gap-5">
                  {group.fields.map(field => {
                    const isCityField = field.key === 'city';
                    const fieldProps = { ...field };

                    if (isCityField && cityOptions.length > 0) {
                      fieldProps.type = 'creatable-select';
                    }

                    return (
                      <FormField
                        key={field.key}
                        field={fieldProps}
                        control={control}
                        disabled={isView || isSubmitting || (isLoadingPincode && ['state', 'district', 'city', 'country'].includes(field.key))}
                        error={errors[field.key]?.message}
                        options={isCityField && cityOptions.length > 0 ? cityOptions : partyMasterLookups[field.key] || field.options}
                        onAddOption={(newOption) => {
                          if (isCityField) setCityOptions(prev => prev.includes(newOption) ? prev : [...prev, newOption]);
                          else addPartyMasterLookupOption(field.key, newOption);
                        }}
                        onRenameOption={(oldOption, newOption) => {
                          if (isCityField) {
                            setCityOptions(prev => prev.map(option => option === oldOption ? newOption : option));
                            if (watch('city') === oldOption) setValue('city', newOption);
                          } else {
                            renamePartyMasterLookupOption(field.key, oldOption, newOption);
                          }
                        }}
                        onDeleteOption={(option) => {
                          if (isCityField) {
                            setCityOptions(prev => prev.filter(item => item !== option));
                            if (watch('city') === option) setValue('city', '');
                            return true;
                          }
                          return deletePartyMasterLookupOption(field.key, option);
                        }}
                        isLoadingPincode={isLoadingPincode}
                      />
                    );
                  })}
                </div>
              </section>
            ))}
          </div>

          {!isView && (
            <div className="flex flex-col sm:flex-row sm:items-center sm:justify-between gap-3 mt-8 pt-6 border-t border-slate-100">
              <div>
                {!isAdd && (
                  <button
                    type="button"
                    onClick={async () => {
                      if (window.confirm(`Are you sure you want to delete party "${party?.partyName || ''}"? This action cannot be undone.`)) {
                        const success = await deletePartyMaster(party.id);
                        if (success) onBack();
                      }
                    }}
                    className="flex items-center justify-center gap-2 px-5 py-3 rounded-xl text-sm font-bold text-red-600 bg-red-50 hover:bg-red-100 border border-red-200 transition-all"
                  >
                    <Trash2 size={16} />
                    Delete Party
                  </button>
                )}
              </div>
              <div className="flex items-center gap-3">
                <button
                  type="button"
                  onClick={onBack}
                  className="flex items-center justify-center gap-2 px-6 py-3 rounded-xl text-sm font-bold text-slate-600 border border-slate-200 bg-white hover:bg-slate-50 transition-all"
                >
                  <X size={16} />
                  Cancel
                </button>
                <button
                  type="submit"
                  disabled={isSubmitting}
                  className="btn-primary flex items-center justify-center gap-2 px-8 py-3 rounded-xl text-sm font-bold text-white shadow-lg shadow-emerald-500/30"
                >
                  {isSubmitting ? <SlidersHorizontal size={16} className="spin" /> : <Save size={16} />}
                  {isAdd ? 'Create Party' : 'Save Changes'}
                </button>
              </div>
            </div>
          )}
        </form>
      </div>
    </div>
  );
}

